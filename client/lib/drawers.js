/**
 * Toggle Bottom Drawer
 * @param drawerId
 */
toggleBottomDrawer = function( drawerId ){
    var menuBottom = document.getElementById( drawerId );
    classie.toggle( menuBottom , 'spmenu-open');
};


openBottomDrawer = function(){
    var menubottom = document.getElementById( drawerId );
    classie.toggle( menubottom, 'spmenu-open' );
};